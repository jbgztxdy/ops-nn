def bn_infer_grad_numpy(grads, scale, variance, epsilon, format_mode):
    """
    x_backprop = grads * scale * 1 / sqrt(batch_variance + epsilon)
    grads:    NCHW (mode=0), NHWC (mode=1), NC1HWC0 (mode=2)
    scale:    [C] for mode 0/1; [C0] for mode 2 (per C1 slice broadcast)
    variance: 同 scale
    """
    inv_std = 1.0 / np.sqrt(variance.astype(np.float32) + epsilon)
    coeff = scale.astype(np.float32) * inv_std  # [C]
    grads_fp32 = grads.astype(np.float32)
    if format_mode == 0:
        # NCHW: broadcast over (N, 1, H, W)
        broadcast = coeff.reshape(1, -1, 1, 1)
    elif format_mode == 1:
        # NHWC: broadcast over (N, H, W, 1)
        broadcast = coeff.reshape(1, 1, 1, -1)
    else:
        # NC1HWC0: coeff per (C1, C0); broadcast over (N, C1, 1, 1, C0)
        # coeff length = C1 * C0
        c1 = grads_fp32.shape[1]
        c0 = grads_fp32.shape[4]
        broadcast = coeff.reshape(1, c1, 1, 1, c0)
    return grads_fp32 * broadcast


def fp32_to_bf16_bytes(fp32_array):
    """将 fp32 数组转换为 bf16 字节表示（uint16）"""
    data_bytes = fp32_array.astype(np.float32).tobytes()
    result = []
    for i in range(0, len(data_bytes), 4):
        bf16_bytes = data_bytes[i + 2:i + 4]
        result.append(struct.unpack('<H', bf16_bytes)[0])
    return np.array(result, dtype=np.uint16)


def bf16_bytes_to_fp32(bf16_array):
    """将 bf16 uint16 数组反序列化为 fp32"""
    fp32_list = []
    for v in bf16_array:
        b = struct.pack('<H', v)
        fp32_bytes = b'\x00\x00' + b
        fp32_list.append(struct.unpack('<f', fp32_bytes)[0])
    return np.array(fp32_list, dtype=np.float32)


