int CreateOppInGraph(
    DataType inDtype, std::vector<ge::Tensor>& input, std::vector<Operator>& inputs, std::vector<Operator>& outputs,
    Graph& graph)
{
    auto op0 = op::SparseApplyFtrlV2("sparse_apply_ftrl_v21");

    const int64_t rowNum = 4;
    std::vector<int64_t> tensorShape = {rowNum, 8};  // var / accum / linear shape (N=4, inner=8)
    std::vector<int64_t> gradShape = {2, 8};         // grad shape (M=2, inner=8)
    std::vector<int64_t> indicesShape = {2};         // indices shape (M=2)
    std::vector<int64_t> scalarShape = {1};          // lr / l1 / l2 / l2_shrinkage / lr_power

    int idx = 0;
    // 10 inputs — order must match REG_OP(SparseApplyFtrlV2) .INPUT order
    if (AddDataInput(idx++, [&](Operator& d) { op0.set_input_var(d); }, inDtype, tensorShape, 1.0f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { op0.set_input_accum(d); }, inDtype, tensorShape, 1.0f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { op0.set_input_linear(d); }, inDtype, tensorShape, 0.0f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { op0.set_input_grad(d); }, inDtype, gradShape, 0.2f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddIndicesInput(idx++, [&](Operator& d) { op0.set_input_indices(d); }, indicesShape, rowNum,
                        graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { op0.set_input_lr(d); }, inDtype, scalarShape, 0.01f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { op0.set_input_l1(d); }, inDtype, scalarShape, 0.0f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { op0.set_input_l2(d); }, inDtype, scalarShape, 0.0f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { op0.set_input_l2_shrinkage(d); }, inDtype, scalarShape, 0.0f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { op0.set_input_lr_power(d); }, inDtype, scalarShape, -0.5f,
                     graph, input, inputs) != SUCCESS) return FAILED;

    op0.set_attr_use_locking(false);

    // 3 outputs (var, accum, linear)
    TensorDesc outDesc = TensorDesc(ge::Shape(tensorShape), FORMAT_ND, inDtype);
    op0.update_output_desc_var(outDesc);
    op0.update_output_desc_accum(outDesc);
    op0.update_output_desc_linear(outDesc);

    outputs.push_back(op0);
    return SUCCESS;
}

int main(int argc, char* argv[])
{
    const char* graph_name = "tc_sparse_apply_ftrl_v2_ge_irrun_test";
    Graph graph(graph_name);
    std::vector<ge::Tensor> input;

    printf("%s - INFO - [XIR]: Start to initialize ge using ge global options\n", GetTime().c_str());
    std::map<AscendString, AscendString> global_options = {{"ge.exec.deviceId", "0"}, {"ge.graphRunMode", "1"}};
    Status ret = ge::GEInitialize(global_options);
    if (ret != SUCCESS) {
        printf("%s - INFO - [XIR]: Initialize ge using ge global options failed\n", GetTime().c_str());
        return FAILED;
    }
    printf("%s - INFO - [XIR]: Initialize ge using ge global options success\n", GetTime().c_str());

    std::vector<Operator> inputs{};
    std::vector<Operator> outputs{};

    if (argc > 1) {
        std::cout << "device id arg: " << argv[1] << std::endl;
    }

    DataType inDtype = DT_FLOAT;
    std::cout << "input dtype: " << inDtype << std::endl;

    ret = CreateOppInGraph(inDtype, input, inputs, outputs, graph);
    if (ret != SUCCESS) {
        printf("%s - ERROR - [XIR]: Create ir session using build options failed\n", GetTime().c_str());
        return FAILED;
    }

    if (!inputs.empty() && !outputs.empty()) {
        graph.SetInputs(inputs).SetOutputs(outputs);
    }

    std::map<AscendString, AscendString> build_options = {};
    printf("%s - INFO - [XIR]: Start to create ir session using build options\n", GetTime().c_str());
    ge::Session* session = new Session(build_options);
    if (session == nullptr) {
        printf("%s - ERROR - [XIR]: Create ir session using build options failed\n", GetTime().c_str());
        return FAILED;
    }
    printf("%s - INFO - [XIR]: Create ir session using build options success\n", GetTime().c_str());

    std::map<AscendString, AscendString> graph_options = {};
    uint32_t graph_id = 0;
    ret = session->AddGraph(graph_id, graph, graph_options);
    printf("%s - INFO - [XIR]: Session add ir compute graph to ir session success\n", GetTime().c_str());
    std::string file_path = "./dump";
    aclgrphDumpGraph(graph, file_path.c_str(), file_path.length());
    printf("%s - INFO - [XIR]: Start to run ir compute graph\n", GetTime().c_str());
    std::vector<ge::Tensor> output;
    ret = session->RunGraph(graph_id, input, output);
    if (ret != SUCCESS) {
        printf("%s - INFO - [XIR]: Run graph failed\n", GetTime().c_str());
        delete session;
        GEFinalize();
        return FAILED;
    }
    printf("%s - INFO - [XIR]: Session run ir compute graph success\n", GetTime().c_str());

    int input_num = input.size();
    for (int i = 0; i < input_num; i++) {
        string input_file = "./tc_sparse_apply_ftrl_v2_ge_irrun_test_npu_input_" + std::to_string(i) + ".bin";
        uint8_t* input_data_i = input[i].GetData();
        int64_t input_shape = input[i].GetTensorDesc().GetShape().GetShapeSize();
        uint32_t data_size = input_shape * GetDataTypeSize(input[i].GetTensorDesc().GetDataType());
        WriteDataToFile((const char*)input_file.c_str(), data_size, input_data_i);
    }

    int output_num = output.size();
    for (int i = 0; i < output_num; i++) {
        string output_file = "./tc_sparse_apply_ftrl_v2_ge_irrun_test_npu_output_" + std::to_string(i) + ".bin";
        uint8_t* output_data_i = output[i].GetData();
        int64_t output_shape = output[i].GetTensorDesc().GetShape().GetShapeSize();
        uint32_t data_size = output_shape * GetDataTypeSize(output[i].GetTensorDesc().GetDataType());
        WriteDataToFile((const char*)output_file.c_str(), data_size, output_data_i);
    }

    ge::AscendString error_msg = ge::GEGetErrorMsgV2();
    std::cout << "Error message: " << std::string(error_msg.GetString()) << std::endl;
    printf("%s - INFO - [XIR]: Precision is ok\n", GetTime().c_str());
    ret = ge::GEFinalize();
    if (ret != SUCCESS) {
        printf("%s - INFO - [XIR]: Finalize ir graph session failed\n", GetTime().c_str());
        return FAILED;
    }
    printf("%s - INFO - [XIR]: Finalize ir graph session success\n", GetTime().c_str());
    delete session;
    return SUCCESS;
}
