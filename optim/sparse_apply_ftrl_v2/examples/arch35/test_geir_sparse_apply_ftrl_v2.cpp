uint32_t GetDataTypeSize(DataType dt)
{
    if (dt == ge::DT_FLOAT) {
        return 4;
    } else if (dt == ge::DT_FLOAT16 || dt == ge::DT_BF16) {
        return 2;
    } else if (dt == ge::DT_INT32) {
        return 4;
    } else if (dt == ge::DT_INT64) {
        return 8;
    }
    return 4;
}

int32_t GenFloatData(
    vector<int64_t> shapes, Tensor& input_tensor, TensorDesc& input_tensor_desc, DataType data_type, float value)
{
    input_tensor_desc.SetRealDimCnt(shapes.size());
    size_t size = 1;
    for (uint32_t i = 0; i < shapes.size(); i++) {
        size *= shapes[i];
    }
    uint32_t data_len = size * GetDataTypeSize(data_type);
    float* pData = new (std::nothrow) float[size];
    if (pData == nullptr) {
        printf("GenFloatData: allocation failed for size=%zu\n", size);
        return FAILED;
    }
    for (size_t i = 0; i < size; ++i) {
        *(pData + i) = value;
    }
    input_tensor = Tensor(input_tensor_desc, reinterpret_cast<uint8_t*>(pData), data_len);
    delete[] pData;
    return SUCCESS;
}

// indices 用合法行号填充：第 i 个索引取 i % rowNum，保证落在 [0, rowNum)
int32_t GenInt32Data(
    vector<int64_t> shapes, Tensor& input_tensor, TensorDesc& input_tensor_desc, int64_t rowNum)
{
    input_tensor_desc.SetRealDimCnt(shapes.size());
    size_t size = 1;
    for (uint32_t i = 0; i < shapes.size(); i++) {
        size *= shapes[i];
    }
    uint32_t data_len = size * GetDataTypeSize(ge::DT_INT32);
    int32_t* pData = new (std::nothrow) int32_t[size];
    if (pData == nullptr) {
        printf("GenInt32Data: allocation failed for size=%zu\n", size);
        return FAILED;
    }
    int64_t denom = (rowNum > 0) ? rowNum : 1;
    for (size_t i = 0; i < size; ++i) {
        *(pData + i) = static_cast<int32_t>(i % denom);
    }
    input_tensor = Tensor(input_tensor_desc, reinterpret_cast<uint8_t*>(pData), data_len);
    delete[] pData;
    return SUCCESS;
}

int32_t WriteDataToFile(string bin_file, uint64_t data_size, uint8_t* inputData)
{
    FILE* fp = fopen(bin_file.c_str(), "wb");
    if (fp == nullptr) {
        printf("WriteDataToFile: fopen failed for %s\n", bin_file.c_str());
        return FAILED;
    }
    size_t written = fwrite(inputData, sizeof(uint8_t), data_size, fp);
    fclose(fp);
    if (written != data_size) {
        printf("WriteDataToFile: short write %zu/%lu\n", written, data_size);
        return FAILED;
    }
    return SUCCESS;
}

template <typename SetterFn>
int32_t AddDataInput(
    int placeholderIndex, SetterFn portSetter, DataType dtype, const vector<int64_t>& shape, float fillValue,
    Graph& graph, std::vector<ge::Tensor>& input, std::vector<Operator>& inputs)
{
    std::string name = "placeholder" + std::to_string(placeholderIndex);
    auto data = op::Data(name.c_str()).set_attr_index(placeholderIndex);
    TensorDesc desc = TensorDesc(ge::Shape(shape), FORMAT_ND, dtype);
    desc.SetPlacement(ge::kPlacementHost);
    desc.SetFormat(FORMAT_ND);
    Tensor tensor;
    Status ret = GenFloatData(shape, tensor, desc, dtype, fillValue);
    if (ret != SUCCESS) {
        printf("%s - ERROR - [XIR]: Generate input data failed for %s\n", GetTime().c_str(), name.c_str());
        return FAILED;
    }
    data.update_input_desc_x(desc);
    input.push_back(tensor);
    graph.AddOp(data);
    portSetter(data);
    inputs.push_back(data);
    return SUCCESS;
}

