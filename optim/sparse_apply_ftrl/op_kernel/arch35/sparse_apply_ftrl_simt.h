    T accumVal = accum[rowOffset];
    T varVal = var[rowOffset];
    T linearVal = linear[rowOffset];

    T accumNew = accumVal + g * g;

    T accumNegLrPower = powf(accumVal, negLrPower);
    T accumNewNegLrPower = powf(accumNew, negLrPower);

