// =====================================================================
// T10 Large shape (>1M elements) -> totalNum field correctness
// =====================================================================
TEST_F(TestApplyAdaMaxTiling, apply_adamax_large_shape_total_num)
{
    auto result = DoApplyAdaMaxValidCase({2048, 1024}, ge::DT_FLOAT, /*useLocking=*/false);
    ASSERT_EQ(result.status, ge::GRAPH_SUCCESS);
    ASSERT_EQ(result.tilingKey, TILING_KEY_FP32_ALIGN);
    EXPECT_EQ(result.totalNum, 2048L * 1024);
    // blockFactor 32B aligned (alignElem=8 for fp32)
    EXPECT_EQ(result.blockFactor % 8, 0);
}

// =====================================================================
// T11 0-dim scalar var (rank == 0) -> EnsureNotScalar covers the
// `in_shape.GetDimNum() == 0` branch and promotes shape to {1}; totalIdx
// becomes 1, smaller than alignElem (8 for fp32), so the small-shape
// single-core path is taken (T05 covers the typical small path).
// This closes the L49 coverage of EnsureNotScalar.
// =====================================================================
TEST_F(TestApplyAdaMaxTiling, apply_adamax_zero_dim_scalar_var)
{
    auto result = DoApplyAdaMaxTilingCase(
        /*var=*/{},  // 0-dim scalar
        /*m=*/{},
        /*v=*/{},
        /*beta1_power=*/{1}, /*lr=*/{1}, /*beta1=*/{1}, /*beta2=*/{1}, /*epsilon=*/{1},
        /*grad=*/{},
        ge::DT_FLOAT, ge::FORMAT_ND, /*useLocking=*/false);
    ASSERT_EQ(result.status, ge::GRAPH_SUCCESS);
    // EnsureNotScalar promotes {} to {1}, so totalNum == 1.
    EXPECT_EQ(result.totalNum, 1);
    EXPECT_EQ(result.blockFactor, 1);
}

