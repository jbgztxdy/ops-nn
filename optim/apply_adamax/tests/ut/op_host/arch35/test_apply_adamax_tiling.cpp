/*!
 * \file test_apply_adamax_tiling.cpp
 * \brief Tiling UT for ApplyAdaMax operator (arch35 / ascend950).
 *
 * Schema:
 *   Inputs  (9): var, m, v, beta1_power, lr, beta1, beta2, epsilon, grad
 *     - var / m / v / grad: share var.shape, dtype ∈ {FP16, FP32}
 *     - beta1_power / lr / beta1 / beta2 / epsilon: scalar tensors (shape == {1}), same dtype as var
 *   Outputs (1): var
 *   Attrs   (1): use_locking (optional bool, default false; tiling does not read it)
 *
 * TilingKey encoding (D_T / K_ALIGN via ASCENDC_TPL_SEL_PARAM):
 *   D_T = C_DT_FLOAT   (=0) + K_ALIGN=0 -> key 0
 *   D_T = C_DT_FLOAT   (=0) + K_ALIGN=1 -> key 256
 *   D_T = C_DT_FLOAT16 (=1) + K_ALIGN=0 -> key 1
 *   D_T = C_DT_FLOAT16 (=1) + K_ALIGN=1 -> key 257
 *
 * Alignment:
 *   alignElem = 32B / sizeof(T) -> fp32: 8, fp16: 16
 *   K_ALIGN = (totalNum % alignElem == 0) ? 1 : 0
 *
 * TilingData fields (apply_adamax_tiling_data.h):
 *   - totalNum    : var element total count
 *   - blockFactor : per-core processed elements (32B aligned)
 *   - ubFactor    : per-UB-loop processed elements (256 aligned, >= alignElem)
 *
 * Test coverage (12 cases):
 *   T01 FP32 aligned (totalNum % 8 == 0)        -> key 256
 *   T02 FP32 unaligned (totalNum % 8 != 0)      -> key 0
 *   T03 FP16 aligned (totalNum % 16 == 0)       -> key 257
 *   T04 FP16 unaligned (totalNum % 16 != 0)     -> key 1
 *   T05 Small shape (totalIdx < alignElem)      -> blockFactor == totalIdx, single core
 *   T06 8D shape (rank == 8 boundary)           -> SUCCESS
 *   T07 use_locking == true                     -> ignored by tiling, still SUCCESS
 *   T08 Unsupported dtype (DT_INT32)            -> FAILED
 *   T09 ubFactor != 0 and aligned to 256
 *   T10 totalNum field correctness for >1M elements
 *   T11 0-dim scalar var (rank == 0)            -> EnsureNotScalar promotes to {1}, SUCCESS
 *   T12 Empty Tensor (totalIdx == 0)            -> SUCCESS with zero-work tiling
 */

