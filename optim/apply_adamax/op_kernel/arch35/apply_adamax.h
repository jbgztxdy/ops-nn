    // 详见 issues/2026-05-23_scalar_getvalue_waiver.md（后续补档）。
    beta1Power_ = static_cast<float>(beta1PowerGm.GetValue(0));
    lr_ = static_cast<float>(lrGm.GetValue(0));
    beta1_ = static_cast<float>(beta1Gm.GetValue(0));
    beta2_ = static_cast<float>(beta2Gm.GetValue(0));
    epsilon_ = static_cast<float>(epsilonGm.GetValue(0));

    oneMinusBeta1_ = 1.0f - beta1_;
