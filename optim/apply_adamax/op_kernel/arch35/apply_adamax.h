    LocalTensor<T> mOutLocal = outQueM.template DeQue<T>();
    LocalTensor<T> vOutLocal = outQueV.template DeQue<T>();

    DataCopyExtParams copyParams{1, static_cast<uint32_t>(currentNum * sizeof(T)), 0, 0, 0};
