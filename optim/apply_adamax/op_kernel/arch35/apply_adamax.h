    // 内部 clamp"保持一致）。
    constexpr float EPS_FLOOR = 1.1754944e-38f; // FLT_MIN
    if (epsilon_ >= 0.0f && epsilon_ < EPS_FLOOR) {
        epsilon_ = EPS_FLOOR;
    }
