int32_t GenScalarConstData(Tensor& tensor, TensorDesc& desc, DataType dtype, float value)
{
    vector<int64_t> shape = {1};
    desc.SetRealDimCnt(1);
    uint32_t dataLen = GetDataTypeSize(dtype);
    uint8_t* pData = new (std::nothrow) uint8_t[dataLen];
    if (dtype == ge::DT_FLOAT) {
        float v = value;
        memcpy(pData, &v, sizeof(float));
    } else if (dtype == ge::DT_FLOAT16) {
        uint16_t v = 0;
        float f = value;
        uint32_t* fi = reinterpret_cast<uint32_t*>(&f);
        uint32_t sign = (*fi >> 16) & 0x8000;
        uint32_t exp = ((*fi >> 23) & 0xFF) - 127 + 15;
        uint32_t mant = (*fi >> 13) & 0x3FF;
        if (exp <= 0) v = sign;
        else if (exp >= 31) v = sign | 0x7C00;
        else v = sign | (exp << 10) | mant;
        memcpy(pData, &v, sizeof(uint16_t));
    } else if (dtype == ge::DT_BF16) {
        // BF16 与 FP32 共享相同的 8-bit 指数（偏置 127），直接截断 FP32 低 16 位 mantissa
        uint16_t v = 0;
        float f = value;
        uint32_t* fi = reinterpret_cast<uint32_t*>(&f);
        v = static_cast<uint16_t>((*fi >> 16) & 0xFFFF);
        memcpy(pData, &v, sizeof(uint16_t));
    } else {
        float v = value;
        memcpy(pData, &v, sizeof(float));
    }
    tensor = Tensor(desc, pData, dataLen);
    delete[] pData;
    return SUCCESS;
}

