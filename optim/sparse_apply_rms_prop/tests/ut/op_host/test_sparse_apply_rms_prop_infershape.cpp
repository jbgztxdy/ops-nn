static std::vector<int64_t> ShapeToVec(const gert::Shape* s)
{
    std::vector<int64_t> v;
    if (s == nullptr) return v;
    for (size_t i = 0; i < s->GetDimNum(); ++i) {
        v.push_back(s->GetDim(i));
    }
    return v;
}

// Drive InferShape via the Faker holder; outputs 0/1/2 follow inputs 0/1/2.
static ge::graphStatus RunAndGetOutputShapes(
    gert::StorageShape& varShape,
    gert::StorageShape& gradShape,
    gert::StorageShape& indicesShape,
    gert::StorageShape& scalarShape,
    std::vector<std::vector<int64_t>>& outShapes)
{
    auto* opImpl = gert::OpImplRegistry::GetInstance().GetOpImpl("SparseApplyRMSProp");
    if (opImpl == nullptr) {
        return ge::GRAPH_FAILED;
    }
    auto inferShapeFunc = opImpl->infer_shape;
    if (inferShapeFunc == nullptr) {
        return ge::GRAPH_FAILED;
    }

    gert::StorageShape outVar = {{}, {}};
    gert::StorageShape outMs  = {{}, {}};
    gert::StorageShape outMom = {{}, {}};
    auto holder = gert::InferShapeContextFaker()
                      .NodeIoNum(9, 3)
                      .IrInstanceNum({1, 1, 1, 1, 1, 1, 1, 1, 1})
                      .InputShapes(
                          {&varShape, &varShape, &varShape,
                           &scalarShape, &scalarShape, &scalarShape, &scalarShape,
                           &gradShape, &indicesShape})
                      .OutputShapes({&outVar, &outMs, &outMom})
                      .Build();

    auto* ctx = holder.GetContext<gert::InferShapeContext>();
    auto status = inferShapeFunc(ctx);
    if (status == ge::GRAPH_SUCCESS) {
        outShapes.clear();
        outShapes.push_back(ShapeToVec(ctx->GetOutputShape(0)));
        outShapes.push_back(ShapeToVec(ctx->GetOutputShape(1)));
        outShapes.push_back(ShapeToVec(ctx->GetOutputShape(2)));
    }
    return status;
}

// case_0: 2D shape -> all three outputs follow var shape.
TEST_F(SparseApplyRMSProp, sparse_apply_rms_prop_infershape_case_0)
{
    ASSERT_NE(gert::OpImplRegistry::GetInstance().GetOpImpl("SparseApplyRMSProp"), nullptr);
    gert::StorageShape varShape     = {{4, 8}, {4, 8}};
    gert::StorageShape gradShape    = {{2, 8}, {2, 8}};
    gert::StorageShape indicesShape = {{2}, {2}};
    gert::StorageShape scalarShape  = {{1}, {1}};
    std::vector<std::vector<int64_t>> outShapes;

    ASSERT_EQ(RunAndGetOutputShapes(varShape, gradShape, indicesShape, scalarShape, outShapes),
              ge::GRAPH_SUCCESS);
    ASSERT_EQ(outShapes.size(), 3u);
    EXPECT_EQ(outShapes[0], (std::vector<int64_t>{4, 8}));
    EXPECT_EQ(outShapes[1], (std::vector<int64_t>{4, 8}));
    EXPECT_EQ(outShapes[2], (std::vector<int64_t>{4, 8}));
}

