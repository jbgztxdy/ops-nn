uint32_t GetDataTypeSize(DataType dt)
{
    uint32_t dilation = 1;
    uint32_t oneByte = 1;
    uint32_t twoByte = 2;
    uint32_t fourByte = 4;
    uint32_t eightByte = 8;

    if (dt == ge::DT_FLOAT) {
        dilation = fourByte;
    } else if (dt == ge::DT_FLOAT16) {
        dilation = twoByte;
    } else if (dt == ge::DT_BF16) {
        dilation = twoByte;
    } else if (dt == ge::DT_INT16) {
        dilation = twoByte;
    } else if (dt == ge::DT_UINT16) {
        dilation = twoByte;
    } else if (dt == ge::DT_INT32) {
        dilation = fourByte;
    } else if (dt == ge::DT_UINT32) {
        dilation = fourByte;
    } else if (dt == ge::DT_INT64) {
        dilation = eightByte;
    } else if (dt == ge::DT_UINT64) {
        dilation = eightByte;
    } else if (dt == ge::DT_INT8) {
        dilation = oneByte;
    }
    return dilation;
}

int32_t GenFloatData(
    vector<int64_t> shapes, Tensor& input_tensor, TensorDesc& input_tensor_desc, DataType data_type, float value)
{
    input_tensor_desc.SetRealDimCnt(shapes.size());
    size_t size = 1;
    for (uint32_t i = 0; i < shapes.size(); i++) {
        size *= shapes[i];
    }
    uint32_t data_len = size * GetDataTypeSize(data_type);
    // new 后立即在 Tensor 拷贝完成后释放，避免 host 端内存泄漏
    float* pData = new (std::nothrow) float[size];
    if (pData == nullptr) {
        printf("GenFloatData: allocation failed for size=%zu\n", size);
        return FAILED;
    }
    for (size_t i = 0; i < size; ++i) {
        *(pData + i) = value;
    }
    input_tensor = Tensor(input_tensor_desc, reinterpret_cast<uint8_t*>(pData), data_len);
    delete[] pData;  // GE Tensor 构造已拷贝，立即释放
    return SUCCESS;
}

int32_t WriteDataToFile(string bin_file, uint64_t data_size, uint8_t* inputData)
{
    FILE* fp = fopen(bin_file.c_str(), "wb");
    if (fp == nullptr) {
        printf("WriteDataToFile: fopen failed for %s\n", bin_file.c_str());
        return FAILED;
    }
    size_t written = fwrite(inputData, sizeof(uint8_t), data_size, fp);
    fclose(fp);
    if (written != data_size) {
        printf("WriteDataToFile: short write %zu/%lu\n", written, data_size);
        return FAILED;
    }
    return SUCCESS;
}

// 构造一个 GE Data 输入（host 占位，device 填充 tensor），并绑定到 op 的指定输入端口
template <typename SetterFn>
int32_t AddDataInput(
    int placeholderIndex, SetterFn portSetter, DataType dtype, const vector<int64_t>& shape, float fillValue,
    Graph& graph, std::vector<ge::Tensor>& input, std::vector<Operator>& inputs)
{
    std::string name = "placeholder" + std::to_string(placeholderIndex);
    auto data = op::Data(name.c_str()).set_attr_index(placeholderIndex);
    TensorDesc desc = TensorDesc(ge::Shape(shape), FORMAT_ND, dtype);
    desc.SetPlacement(ge::kPlacementHost);
    desc.SetFormat(FORMAT_ND);
    Tensor tensor;
    Status ret = GenFloatData(shape, tensor, desc, dtype, fillValue);
    if (ret != SUCCESS) {
        printf("%s - ERROR - [XIR]: Generate input data failed for %s\n", GetTime().c_str(), name.c_str());
        return FAILED;
    }
    data.update_input_desc_x(desc);
    input.push_back(tensor);
    graph.AddOp(data);
    portSetter(data);
    inputs.push_back(data);
    return SUCCESS;
}

int CreateOppInGraph(
    DataType inDtype, std::vector<ge::Tensor>& input, std::vector<Operator>& inputs, std::vector<Operator>& outputs,
    Graph& graph)
{
    // 自定义代码：添加 ApplyAddSign 单算子定义到图中
    auto addsign = op::ApplyAddSign("apply_add_sign1");

    // shape 约定
    std::vector<int64_t> tensorShape = {2, 4, 4, 4}; // var / m / grad 的 shape（共 128 元素）
    std::vector<int64_t> scalarShape = {1};          // lr / alpha / sign_decay / beta 的 shape

    int idx = 0;
    // 7 个输入 —— 顺序须与 REG_OP(ApplyAddSign) 的 .INPUT 顺序一致
    if (AddDataInput(idx++, [&](Operator& d) { addsign.set_input_var(d); }, inDtype, tensorShape, 1.0f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { addsign.set_input_m(d); }, inDtype, tensorShape, 0.1f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { addsign.set_input_lr(d); }, inDtype, scalarShape, 0.01f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { addsign.set_input_alpha(d); }, inDtype, scalarShape, 0.9f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { addsign.set_input_sign_decay(d); }, inDtype, scalarShape, 0.5f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { addsign.set_input_beta(d); }, inDtype, scalarShape, 0.9f,
                     graph, input, inputs) != SUCCESS) return FAILED;
    if (AddDataInput(idx++, [&](Operator& d) { addsign.set_input_grad(d); }, inDtype, tensorShape, 0.2f,
                     graph, input, inputs) != SUCCESS) return FAILED;

    // bool 属性
    addsign.set_attr_use_locking(false);

    // 单输出端口（var）的 TensorDesc，shape/dtype 与输入 var 一致
    TensorDesc varOutDesc = TensorDesc(ge::Shape(tensorShape), FORMAT_ND, inDtype);
    addsign.update_output_desc_var(varOutDesc);

    outputs.push_back(addsign);
    return SUCCESS;
}

