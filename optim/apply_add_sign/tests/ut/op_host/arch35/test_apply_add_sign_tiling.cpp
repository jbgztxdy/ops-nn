/*!
 * \file test_apply_add_sign_tiling.cpp
 * \brief Tiling UT for ApplyAddSign operator (arch35 / ascend950).
 *
 * Schema:
 *   Inputs  (7): var, m, lr, alpha, sign_decay, beta, grad
 *     - var / m / grad: share var.shape, dtype in {FP16, FP32, BF16}
 *     - lr / alpha / sign_decay / beta: scalar tensors (shape == {1}), same dtype as var
 *   Outputs (1): var
 *   Attrs   (1): use_locking (optional bool, default false, semantic placeholder)
 *
 * TilingKey encoding (single param D_T_X via ASCENDC_TPL_SEL_PARAM):
 *   host: ASCENDC_TPL_SEL_PARAM(context, static_cast<uint32_t>(dataType))
 *   key = GE DataType enum value passed down:
 *     DT_FLOAT   (=0)  -> key 0    (apply_add_sign<float>)
 *     DT_FLOAT16 (=1)  -> key 1    (apply_add_sign<half>)
 *     DT_BF16    (=27) -> key 27   (apply_add_sign<bfloat16_t>)
 *
 * Tiling internals (covers post-acceptance helpers):
 *   - SetEmptyTensorTiling: dim0==0 fast path (BlockDim=1, ubFactor=blockFactor=1)
 *   - ComputeBlockAndUbSplit: multi-core 32B alignment + UB sizing
 *       fp32 bytesPerElem = 104, fp16/bf16 = 88
 *       MIN_UB_FACTOR_BYTES = 32 (lower bound guard for crash repro)
 *   - ComputeSignTmpSize: GetSignMaxMinTmpSize integration (signTmpSize>=512)
 *
 * Test coverage (16 cases):
 *   T01  FP32 small shape (single-core retreat boundary)
 *   T02  FP32 medium shape (multi-core full split)
 *   T03  FP32 large shape (multi-core stress)
 *   T04  FP16 medium shape (verify ubFactor 88B/elem path)
 *   T05  FP16 small shape (lower bound for fp16)
 *   T06  BF16 medium shape (same path as fp16, verify BF16 dtype enum=27)
 *   T07  BF16 large shape (BF16 multi-core stress)
 *   T08  Empty tensor dim0=0 (SetEmptyTensorTiling path) -- FP32
 *   T09  Empty tensor dim0=0 -- BF16 (cover SetEmptyTensorTiling x BF16)
 *   T10  Scalar shape {1} (lowest non-empty bound)
 *   T11  m.shape mismatch -> FAILED
 *   T12  grad.shape mismatch -> FAILED
 *   T13  lr is NOT 1-element (shape={2}) -> FAILED
 *   T14  use_locking=true (informational, no kernel-side effect)
 *   T15  Rank-8 boundary shape (high-dim path) -> SUCCESS
 *   T16  Unsupported dtype (INT32) -> FAILED
 */

