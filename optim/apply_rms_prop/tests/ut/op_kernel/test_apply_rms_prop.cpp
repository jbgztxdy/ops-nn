// Helper to convert float -> half (IEEE-754 binary16 round-to-nearest-even).
// Reuses the tikicpulib representation that ::apply_rms_prop expects when
// DTYPE is half (uint16_t in CPU memory).
static inline uint16_t Float2Half(float f)
{
    uint32_t x;
    std::memcpy(&x, &f, sizeof(x));
    uint32_t sign = (x >> 31) & 0x1;
    int32_t  exp  = ((x >> 23) & 0xff) - 127 + 15;
    uint32_t mant = x & 0x7fffff;
    uint16_t h;
    if (exp <= 0) {
        // Subnormal / underflow -> +/- 0 (sufficient for our tests).
        h = static_cast<uint16_t>(sign << 15);
    } else if (exp >= 0x1f) {
        // Overflow / Inf / NaN
        h = static_cast<uint16_t>((sign << 15) | (0x1f << 10) | (mant != 0 ? 0x200 : 0));
    } else {
        // Round-to-nearest-even on the 13 truncated bits.
        uint32_t round = mant & 0x1fff;
        uint16_t mantH = static_cast<uint16_t>(mant >> 13);
        if (round > 0x1000 || (round == 0x1000 && (mantH & 0x1))) {
            mantH += 1;
            if (mantH == 0x400) {  // mantissa overflow into exp
                mantH = 0;
                exp += 1;
                if (exp >= 0x1f) {
                    return static_cast<uint16_t>((sign << 15) | (0x1f << 10));
                }
            }
        }
        h = static_cast<uint16_t>((sign << 15) | (exp << 10) | mantH);
    }
    return h;
}

// Helper to convert float -> bfloat16 (truncation, sufficient for UT).
static inline uint16_t Float2BFloat16(float f)
{
    uint32_t x;
    std::memcpy(&x, &f, sizeof(x));
    return static_cast<uint16_t>(x >> 16);
}

// Fill a GM buffer of T elements with the given value, using the appropriate
// in-memory representation (CPU-side stand-in for fp16/bf16).
template <typename T>
static void FillScalar(uint8_t* gm, float value);

template <>
void FillScalar<float>(uint8_t* gm, float value)
{
    auto* p = reinterpret_cast<float*>(gm);
    *p = value;
}

template <>
void FillScalar<half>(uint8_t* gm, float value)
{
    auto* p = reinterpret_cast<uint16_t*>(gm);
    *p = Float2Half(value);
}

template <>
void FillScalar<bfloat16_t>(uint8_t* gm, float value)
{
    auto* p = reinterpret_cast<uint16_t*>(gm);
    *p = Float2BFloat16(value);
}

// Fill an array of n T-typed elements with the same value.
template <typename T>
static void FillArray(uint8_t* gm, size_t n, float value)
{
    for (size_t i = 0; i < n; ++i) {
        FillScalar<T>(gm + i * sizeof(T), value);
    }
}

