// Build a 1-D / N-D shape from a vector for output assertions.
static std::vector<int64_t> ShapeToVec(const gert::Shape* s)
{
    std::vector<int64_t> v;
    if (s == nullptr) return v;
    for (size_t i = 0; i < s->GetDimNum(); ++i) {
        v.push_back(s->GetDim(i));
    }
    return v;
}

// Drive InferShape via the Faker holder, then read the output shape back
// through the holder's context (since the Faker copies the input/output
// StorageShape arguments — writes inside the InferShape function don't
// propagate back to the caller-side variables).
static ge::graphStatus RunAndGetOutputShape(
    gert::StorageShape& tensorShape,
    gert::StorageShape& scalarShape,
    std::vector<int64_t>& outputDims)
{
    auto* opImpl = gert::OpImplRegistry::GetInstance().GetOpImpl("ApplyRMSProp");
    if (opImpl == nullptr) {
        return ge::GRAPH_FAILED;
    }
    auto inferShapeFunc = opImpl->infer_shape;
    if (inferShapeFunc == nullptr) {
        return ge::GRAPH_FAILED;
    }

    // outputShape placeholder — only used by the Faker for layout; the actual
    // value is read back from the context after the call.
    gert::StorageShape outputShape = {{}, {}};
    auto holder = gert::InferShapeContextFaker()
                      .NodeIoNum(8, 1)
                      .IrInstanceNum({1, 1, 1, 1, 1, 1, 1, 1})
                      .InputShapes(
                          {&tensorShape, &tensorShape, &tensorShape,
                           &scalarShape, &scalarShape, &scalarShape, &scalarShape,
                           &tensorShape})
                      .OutputShapes({&outputShape})
                      .Build();

    auto* ctx = holder.GetContext<gert::InferShapeContext>();
    auto status = inferShapeFunc(ctx);
    if (status == ge::GRAPH_SUCCESS) {
        outputDims = ShapeToVec(ctx->GetOutputShape(0));
    }
    return status;
}

// case_0: 2D shape, single-output var shape inference.
TEST_F(ApplyRMSProp, ApplyRMSProp_infershape_case_0)
{
    ASSERT_NE(gert::OpImplRegistry::GetInstance().GetOpImpl("ApplyRMSProp"), nullptr);
    gert::StorageShape varShape = {{96, 256}, {96, 256}};
    gert::StorageShape scalarShape = {{1}, {1}};
    std::vector<int64_t> outDims;

    ASSERT_EQ(RunAndGetOutputShape(varShape, scalarShape, outDims), ge::GRAPH_SUCCESS);
    ASSERT_EQ(outDims.size(), 2u);
    EXPECT_EQ(outDims[0], 96);
    EXPECT_EQ(outDims[1], 256);
}

