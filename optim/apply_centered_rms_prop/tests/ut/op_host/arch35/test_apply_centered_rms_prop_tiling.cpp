/*!
 * \file test_apply_centered_rms_prop_tiling.cpp
 * \brief Tiling UT for ApplyCenteredRMSProp operator (arch35 / ascend950).
 *
 * Schema:
 *   Inputs  (9): var, mg, ms, mom, lr, rho, momentum, epsilon, grad
 *     - var / mg / ms / mom / grad: share var.shape, dtype ∈ {FP32, FP16, BF16}
 *     - lr / rho / momentum / epsilon: scalar tensors, same dtype as var
 *   Outputs (1): var (mg/ms/mom updated in-place)
 *   Attrs   (1): use_locking (optional bool, default false)
 *
 * TilingData fields (apply_centered_rms_prop_tiling_data.h):
 *   - totalNum, blockFactor, ubFactor : multi-core split params
 *   - lr / rho / momentum / epsilon    : float32, host-read scalars (currently
 *     always 0 because tiling reads from device memory via the disabled
 *     ReadScalarInput; scalarsValid stays 0 and the kernel falls back to GM)
 *   - scalarsValid                     : 0/1
 *
 * Dtype dispatch is handled by the framework via DTYPE_VAR; no tilingKey
 * is set for dtype selection.
 *
 * Validation: shape mismatch on mg/ms/mom/grad -> FAILED. Empty tensor ->
 * SUCCESS fast path with blockDim 1. No scalar-form / dtype-uniformity checks
 * (mirrors the actual tiling impl, which intentionally omits them).
 */

