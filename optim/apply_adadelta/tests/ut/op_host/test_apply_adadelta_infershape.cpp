// case_0: 2D shape, single-output var shape inference.
TEST_F(ApplyAdadelta, ApplyAdadelta_infershape_case_0)
{
    ASSERT_NE(gert::OpImplRegistry::GetInstance().GetOpImpl("ApplyAdadelta"), nullptr);
    auto inferShapeFunc = gert::OpImplRegistry::GetInstance().GetOpImpl("ApplyAdadelta")->infer_shape;
    ASSERT_NE(inferShapeFunc, nullptr);
    gert::StorageShape varShape = {{96, 256}, {96, 256}};
    gert::StorageShape accumShape = {{96, 256}, {96, 256}};
    gert::StorageShape accumUpdateShape = {{96, 256}, {96, 256}};
    gert::StorageShape lrShape = {{1}, {1}};
    gert::StorageShape rhoShape = {{1}, {1}};
    gert::StorageShape epsilonShape = {{1}, {1}};
    gert::StorageShape gradShape = {{96, 256}, {96, 256}};

    auto holder = gert::InferShapeContextFaker()
                      .NodeIoNum(7, 1)
                      .IrInstanceNum({1, 1, 1, 1, 1, 1, 1})
                      .InputShapes(
                          {&varShape, &accumShape, &accumUpdateShape, &lrShape, &rhoShape, &epsilonShape, &gradShape})
                      .OutputShapes({&varShape})
                      .Build();

    ASSERT_EQ(inferShapeFunc(holder.GetContext<gert::InferShapeContext>()), ge::GRAPH_SUCCESS);
}

