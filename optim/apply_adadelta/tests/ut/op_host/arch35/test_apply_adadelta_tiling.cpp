struct ApplyAdadeltaUtCompileInfo {};

// Build a TilingContext for ApplyAdadelta with the 7-input / 1-output schema
// and run the registered tiling function. Returns the resulting tiling status,
// tiling key and (when status == SUCCESS) the parsed TilingData struct fields.
//
// scalar inputs lr/rho/epsilon dtype is fixed to the same as `tensorDtype`
// (matches README: "lr/rho/epsilon dtype 必须与 var 一致").
//
// Caller passes in expected_tiling_key for the SUCCESS path; for FAILED path
// it can pass an arbitrary value (will not be checked).
struct ApplyAdadeltaTilingResult {
    ge::graphStatus status;
    uint64_t tilingKey;
    int32_t useLocking;
    int64_t totalNum;
};

static ApplyAdadeltaTilingResult DoApplyAdadeltaTilingCase(
    const std::initializer_list<int64_t>& varShape,
    const std::initializer_list<int64_t>& accumShape,
    const std::initializer_list<int64_t>& accumUpdateShape,
    const std::initializer_list<int64_t>& lrShape,
    const std::initializer_list<int64_t>& rhoShape,
    const std::initializer_list<int64_t>& epsilonShape,
    const std::initializer_list<int64_t>& gradShape,
    ge::DataType tensorDtype,
    ge::Format inputFormat,
    bool useLocking)
{
    ApplyAdadeltaTilingResult result{ge::GRAPH_FAILED, 0, 0, 0};

    // init platform
    fe::PlatFormInfos platFormInfo;
    map<string, string> socInfos;
    map<string, string> aicoreSpec;
    map<string, string> intrinsics;
    map<string, string> socVersion = {{"Short_SoC_version", "ASCEND950"}};
    InitPlatForm(platFormInfo, socInfos, aicoreSpec, intrinsics, socVersion);

    std::string opType("ApplyAdadelta");
    EXPECT_NE(gert::OpImplRegistry::GetInstance().GetOpImpl(opType.c_str()), nullptr);
    if (gert::OpImplRegistry::GetInstance().GetOpImpl(opType.c_str()) == nullptr) {
        return result;
    }

    auto tiling_func = gert::OpImplRegistry::GetInstance().GetOpImpl(opType.c_str())->tiling;

    // tilingFunc simulate
    auto param = gert::TilingData::CreateCap(4096);
    auto workspace_size_holer = gert::ContinuousVector::Create<size_t>(4096);
    auto ws_size = reinterpret_cast<gert::ContinuousVector*>(workspace_size_holer.get());
    EXPECT_NE(param, nullptr);
    if (param == nullptr) {
        return result;
    }

    gert::StorageShape varStorage = {varShape, varShape};
    gert::StorageShape accumStorage = {accumShape, accumShape};
    gert::StorageShape accumUpdateStorage = {accumUpdateShape, accumUpdateShape};
    gert::StorageShape lrStorage = {lrShape, lrShape};
    gert::StorageShape rhoStorage = {rhoShape, rhoShape};
    gert::StorageShape epsilonStorage = {epsilonShape, epsilonShape};
    gert::StorageShape gradStorage = {gradShape, gradShape};

    ApplyAdadeltaUtCompileInfo compileInfo;

    auto holder = gert::TilingContextFaker()
                      .SetOpType(opType)
                      .NodeIoNum(7, 1)
                      .IrInstanceNum({1, 1, 1, 1, 1, 1, 1})
                      .InputShapes(
                          {&varStorage, &accumStorage, &accumUpdateStorage,
                           &lrStorage, &rhoStorage, &epsilonStorage, &gradStorage})
                      .OutputShapes({&varStorage})
                      .CompileInfo(&compileInfo)
                      .PlatformInfo(reinterpret_cast<char*>(&platFormInfo))
                      .NodeInputTd(0, tensorDtype, inputFormat, inputFormat)  // var
                      .NodeInputTd(1, tensorDtype, inputFormat, inputFormat)  // accum
                      .NodeInputTd(2, tensorDtype, inputFormat, inputFormat)  // accum_update
                      .NodeInputTd(3, tensorDtype, inputFormat, inputFormat)  // lr
                      .NodeInputTd(4, tensorDtype, inputFormat, inputFormat)  // rho
                      .NodeInputTd(5, tensorDtype, inputFormat, inputFormat)  // epsilon
                      .NodeInputTd(6, tensorDtype, inputFormat, inputFormat)  // grad
                      .NodeOutputTd(0, tensorDtype, inputFormat, inputFormat) // var
                      .NodeAttrs(
                          {{"use_locking", Ops::NN::AnyValue::CreateFrom<bool>(useLocking)}})
                      .TilingData(param.get())
                      .Workspace(ws_size)
                      .Build();

    gert::TilingContext* tiling_context = holder.GetContext<gert::TilingContext>();
    EXPECT_NE(tiling_context->GetPlatformInfo(), nullptr);
    holder.GetContext<gert::TilingContext>()->GetPlatformInfo()->SetPlatformRes("SoCInfo", socInfos);
    holder.GetContext<gert::TilingContext>()->GetPlatformInfo()->SetPlatformRes("AICoreSpec", aicoreSpec);
    holder.GetContext<gert::TilingContext>()->GetPlatformInfo()->SetCoreNumByCoreType("AICore");
    holder.GetContext<gert::TilingContext>()->GetPlatformInfo()->SetPlatformRes("AICoreintrinsicDtypeMap", intrinsics);
    tiling_context->GetPlatformInfo()->SetPlatformRes("version", socVersion);

    result.status = tiling_func(tiling_context);
    if (result.status == ge::GRAPH_SUCCESS) {
        result.tilingKey = tiling_context->GetTilingKey();
        // Parse TilingData struct. For the empty Tensor fast path the
        // ApplyAdadeltaTilingFunc skips InitTilingData (returns early after
        // SetBlockDim(1)), so the buffer keeps its zero-initialized state
        // from CreateCap. We still parse it the same way; consumers should
        // not interpret these fields for the empty path.
        auto rawTilingData = tiling_context->GetRawTilingData();
        if (rawTilingData != nullptr &&
            rawTilingData->GetDataSize() >= sizeof(ApplyAdadeltaTilingData)) {
            const auto* td = reinterpret_cast<const ApplyAdadeltaTilingData*>(
                rawTilingData->GetData());
            result.useLocking = td->useLocking;
            result.totalNum = td->totalNum;
        }
    }
    return result;
}

// Convenience wrapper: build a "valid" context where all 4 tensor inputs share
// the same shape and lr/rho/epsilon are shape {1}. Returns the tiling result.
static ApplyAdadeltaTilingResult DoApplyAdadeltaValidCase(
    const std::initializer_list<int64_t>& tensorShape,
    ge::DataType tensorDtype,
    bool useLocking = false)
{
    return DoApplyAdadeltaTilingCase(
        tensorShape, tensorShape, tensorShape,
        {1}, {1}, {1},
        tensorShape,
        tensorDtype, ge::FORMAT_ND, useLocking);
}

