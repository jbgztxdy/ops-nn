/*!
 * \file test_apply_adadelta_tiling.cpp
 * \brief Tiling UT for ApplyAdadelta operator (arch35 / ascend950).
 *
 * Schema:
 *   Inputs  (7): var, accum, accum_update, lr, rho, epsilon, grad
 *     - var / accum / accum_update / grad: share var.shape, dtype ∈ {FP16, FP32}
 *     - lr / rho / epsilon: scalar tensors (shape == {1}), same dtype as var
 *   Outputs (1): var
 *   Attrs   (1): use_locking (optional bool, default false)
 *
 * TilingKey encoding (D_T_X / BUFFER_MODE via ASCENDC_TPL_SEL_PARAM):
 *   D_T_X = C_DT_FLOAT   (=0) + BUFFER_MODE=0  -> key 0
 *   D_T_X = C_DT_FLOAT   (=0) + BUFFER_MODE=1  -> key 256
 *   D_T_X = C_DT_FLOAT16 (=1) + BUFFER_MODE=0  -> key 1
 *   D_T_X = C_DT_FLOAT16 (=1) + BUFFER_MODE=1  -> key 257
 *
 * Threshold:
 *   MIN_SPLIT_THRESHOLD = 1024:
 *     totalNum <= 1024 -> single buffer (BUFFER_MODE = 0)
 *     totalNum >  1024 -> double buffer (BUFFER_MODE = 1)
 *
 * Test coverage (16 cases):
 *   T01 FP32 single buffer (small shape)
 *   T02 FP32 double buffer (large shape)
 *   T03 FP16 single buffer (small shape)
 *   T04 FP16 double buffer (large shape)
 *   T05 totalNum == 0 (empty Tensor fast path) -- FP32
 *   T06 totalNum == 1024 boundary (single buffer)
 *   T07 totalNum == 1025 boundary (double buffer)
 *   T08 C1 shape mismatch: accum.shape != var.shape -> FAILED
 *   T09 C1 shape mismatch: accum_update.shape != var.shape -> FAILED
 *   T10 C1 shape mismatch: grad.shape != var.shape -> FAILED
 *   T11 C3 scalar form: lr.shape != {1} -> FAILED
 *   T12 use_locking == true  -> TilingData.useLocking == 1
 *   T13 use_locking == false -> TilingData.useLocking == 0
 *   T14 rank == 9 (out of [1, 8]) -> FAILED
 *   T15 8D shape (rank == 8 boundary) -> SUCCESS
 *   T16 rank == 0 (0-dim scalar var, lower-bound of [1, 8]) -> FAILED
 */

