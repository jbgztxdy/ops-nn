        pipe_.InitBuffer(varF32Buf_,   ubFactor_ * sizeof(float));
        pipe_.InitBuffer(accumF32Buf_, ubFactor_ * sizeof(float));
        pipe_.InitBuffer(gradF32Buf_,  ubFactor_ * sizeof(float));
