    // fp32 scratch (both paths share).
    pipe_.InitBuffer(etaBuf_,    ubFactor_ * sizeof(float));
    pipe_.InitBuffer(proxBuf_,   ubFactor_ * sizeof(float));
    pipe_.InitBuffer(threshBuf_, ubFactor_ * sizeof(float));
