/**
 * \file apply_proximal_adagrad.h
 * \brief ApplyProximalAdagrad kernel class (arch35 / Ascend950).
 *
 * Implements the Proximal Adagrad update per element:
 *
 *   accum = accum + grad * grad
 *   eta   = lr * rsqrt(accum)
 *   prox  = var - eta * grad
 *   if (l1 > 0):
 *     var = sign(prox) * max(|prox| - eta*l1, 0) / (1 + eta*l2)
 *   else:
 *     var = prox / (1 + eta*l2)
 *
 * Routes (D_T_VAR, PAD_TAIL, HAS_L1):
 *   - D_T_VAR = float       : direct fp32 compute, no Cast.
 *   - D_T_VAR = half        : fp16 input -> Cast to fp32 (CAST_NONE) on CopyIn,
 *       compute in fp32, Cast back to fp16 (CAST_RINT) on CopyOut.
 *       This mirrors canndev A2 TBE (apply_proximal_adagrad_d.py:97-131).
 *   - D_T_VAR = bfloat16_t  : bf16 input -> Cast to fp32 (CAST_NONE) on CopyIn,
 *       compute in fp32, Cast back to bf16 (CAST_ROUND) on CopyOut.
 *       LoadScalar uses a 32B-aligned LocalTensor + Vector Cast to convert
 *       lr/l1/l2 from bf16 GM to fp32 register, avoiding the scalar-path
 *       bf16->float cast that triggers "not support bf16 type cast" on the
 *       bisheng backend.
 *
 *   - PAD_TAIL: 0 (aligned tail) / 1 (non-aligned tail).  Both branches use
 *     DataCopyPad which is alignment-tolerant; the flag is preserved so future
 *     iterations can swap in a faster pure-DataCopy fast path on PAD_TAIL=0.
 *   - HAS_L1: 1 (general path with sign + soft-threshold).  The kernel also
 *     applies a runtime fast-path when l1Scalar_ == 0 inside this branch (this
 *     is the path taken when host cannot read l1 from GM at tiling time).
 *   - HAS_L1: 0 (dedicated fast path - sign / soft-threshold statically
 *     dropped).  Reachable via host-side hint or UT.
 */

