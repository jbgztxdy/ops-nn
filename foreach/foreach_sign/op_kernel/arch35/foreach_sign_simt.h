/**
 * @brief SIMT VF function: grid-stride parallel sign for a single tensor
 * sign(x) = (T(0) < x) - (x < T(0))
 *   x > 0: 1 - 0 = 1
 *   x == 0: 0 - 0 = 0 (including +0 and -0)
 *   x < 0: 0 - 1 = -1
 *   NaN: 0 - 0 = 0 (IEEE 754: NaN comparisons return false)
 */
