// Helper: run tiling and return raw TilingData bytes
static std::vector<uint8_t> RunTilingAndCapture(int64_t numBits, bool narrowRange, const gert::StorageShape& gradShape,
                                                const gert::StorageShape& xShape, const gert::StorageShape& minShape,
                                                const gert::StorageShape& maxShape, ge::DataType ioDtype = ge::DT_FLOAT)
{
    fe::PlatFormInfos platform_info;
    map<string, string> soc_infos;
    map<string, string> aicore_spec;
    map<string, string> intrinsics;
    InitPlatform(platform_info, soc_infos, aicore_spec, intrinsics);
    map<string, string> socversions = {{"Short_SoC_version", "Ascend950"}, {"NpuArch", "3510"}};
    string compile_info_string = R"({
        "hardware_info": {"BT_SIZE": 0, "load3d_constraints": "1",
                          "Intrinsic_fix_pipe_l0c2out": false,
                          "Intrinsic_data_move_l12ub": true,
                          "Intrinsic_data_move_l0c2ub": true,
                          "Intrinsic_data_move_out2l1_nd2nz": false,
                          "UB_SIZE": 253952, "L2_SIZE": 33554432, "L1_SIZE": 524288,
                          "L0A_SIZE": 65536, "L0B_SIZE": 65536, "L0C_SIZE": 131072,
                          "CORE_NUM": 48}
                          })";

    gert::StorageShape outYShape = xShape;
    gert::StorageShape outMinShape = {{1}, {1}};
    gert::StorageShape outMaxShape = {{1}, {1}};

    FakeQuantWithMinMaxVarsGradientCompileInfo compile_info;
    std::string op_type("FakeQuantWithMinMaxVarsGradient");
    auto tiling_func = gert::OpImplRegistry::GetInstance().GetOpImpl(op_type.c_str())->tiling;
    auto tiling_parse_func = gert::OpImplRegistry::GetInstance().GetOpImpl(op_type.c_str())->tiling_parse;

    auto kernel_holder = gert::KernelRunContextFaker()
                             .KernelIONum(2, 1)
                             .Inputs({const_cast<char*>(compile_info_string.c_str()),
                                      reinterpret_cast<void*>(&platform_info)})
                             .Outputs({&compile_info})
                             .Build();

    kernel_holder.GetContext<gert::TilingParseContext>()->GetPlatformInfo()->Init();
    kernel_holder.GetContext<gert::TilingParseContext>()->GetPlatformInfo()->SetPlatformRes("SoCInfo", soc_infos);
    kernel_holder.GetContext<gert::TilingParseContext>()->GetPlatformInfo()->SetPlatformRes("AICoreSpec", aicore_spec);
    kernel_holder.GetContext<gert::TilingParseContext>()->GetPlatformInfo()->SetCoreNumByCoreType("AICore");
    kernel_holder.GetContext<gert::TilingParseContext>()->GetPlatformInfo()->SetPlatformRes("AICoreintrinsicDtypeMap",
                                                                                            intrinsics);

    tiling_parse_func(kernel_holder.GetContext<gert::KernelContext>());

    auto param = gert::TilingData::CreateCap(4096);
    auto workspace_size_holder = gert::ContinuousVector::Create<size_t>(16 * 1024 * 1024);
    auto ws_size = reinterpret_cast<gert::ContinuousVector*>(workspace_size_holder.get());

    auto holder = gert::TilingContextFaker()
                      .NodeIoNum(4, 3)
                      .IrInstanceNum({1, 1, 1, 1})
                      .InputShapes(
                          {const_cast<gert::StorageShape*>(&gradShape), const_cast<gert::StorageShape*>(&xShape),
                           const_cast<gert::StorageShape*>(&minShape), const_cast<gert::StorageShape*>(&maxShape)})
                      .OutputShapes({const_cast<gert::StorageShape*>(&outYShape),
                                     const_cast<gert::StorageShape*>(&outMinShape),
                                     const_cast<gert::StorageShape*>(&outMaxShape)})
                      .CompileInfo(&compile_info)
                      .PlatformInfo(reinterpret_cast<char*>(&platform_info))
                      .NodeInputTd(0, ioDtype, ge::FORMAT_ND, ge::FORMAT_ND)
                      .NodeInputTd(1, ioDtype, ge::FORMAT_ND, ge::FORMAT_ND)
                      .NodeInputTd(2, ioDtype, ge::FORMAT_ND, ge::FORMAT_ND)
                      .NodeInputTd(3, ioDtype, ge::FORMAT_ND, ge::FORMAT_ND)
                      .NodeOutputTd(0, ioDtype, ge::FORMAT_ND, ge::FORMAT_ND)
                      .NodeOutputTd(1, ioDtype, ge::FORMAT_ND, ge::FORMAT_ND)
                      .NodeOutputTd(2, ioDtype, ge::FORMAT_ND, ge::FORMAT_ND)
                      .NodeAttrs({{"num_bits", Ops::NN::AnyValue::CreateFrom<int64_t>(numBits)},
                                  {"narrow_range", Ops::NN::AnyValue::CreateFrom<bool>(narrowRange)}})
                      .TilingData(param.get())
                      .Workspace(ws_size)
                      .Build();

    gert::TilingContext* tiling_context = holder.GetContext<gert::TilingContext>();
    tiling_context->GetPlatformInfo()->SetPlatformRes("SoCInfo", soc_infos);
    tiling_context->GetPlatformInfo()->SetPlatformRes("AICoreSpec", aicore_spec);
    tiling_context->GetPlatformInfo()->SetCoreNumByCoreType("AICore");
    tiling_context->GetPlatformInfo()->SetPlatformRes("AICoreintrinsicDtypeMap", intrinsics);
    tiling_context->GetPlatformInfo()->SetPlatformRes("version", socversions);

    tiling_func(tiling_context);

    // Capture raw TilingData bytes
    auto* rawTd = reinterpret_cast<gert::TilingData*>(param.get());
    size_t dataSize = rawTd->GetDataSize();
    std::vector<uint8_t> result(dataSize);
    std::memcpy(result.data(), rawTd->GetData(), dataSize);
    return result;
}

// determinism_0: Tiling determinism - small shape [256], run twice, verify identical
TEST_F(FakeQuantWithMinMaxVarsGradientTilingTest, tiling_determinism_0_small)
{
    gert::StorageShape gradShape = {{256}, {256}};
    gert::StorageShape xShape = {{256}, {256}};
    gert::StorageShape minShape = {{1}, {1}};
    gert::StorageShape maxShape = {{1}, {1}};

    auto run1 = RunTilingAndCapture(8, false, gradShape, xShape, minShape, maxShape);
    auto run2 = RunTilingAndCapture(8, false, gradShape, xShape, minShape, maxShape);

    ASSERT_EQ(run1.size(), run2.size());
    EXPECT_EQ(std::memcmp(run1.data(), run2.data(), run1.size()), 0)
        << "TilingData differs between two runs (small shape [256])";
}

