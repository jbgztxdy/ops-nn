    Cast(yLocal, xHalf, RoundMode::CAST_CEIL, alignedNum);
    Cast(xHalf, yLocal, RoundMode::CAST_NONE, alignedNum);

    // 合并正负分支
    Add(xHalf, xHalf, posRes, alignedNum);

    // half -> int8：溢出回绕（numpy astype(int8) 语义）。
