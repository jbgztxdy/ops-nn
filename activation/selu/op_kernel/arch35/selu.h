    Exp(xHalf, xHalf, alignedNum);
    Adds(xHalf, xHalf, static_cast<half>(-1), alignedNum);
    Muls(xHalf, xHalf, static_cast<half>(SCALE_ALPHA_PRODUCT), alignedNum);

