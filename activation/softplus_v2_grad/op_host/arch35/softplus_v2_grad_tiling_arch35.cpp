// CheckBroadcastShape — 逐维验证 broadcast 兼容性
bool CheckBroadcastShape(const std::vector<std::vector<int64_t>>& padded_in,
                         const std::vector<std::vector<int64_t>>& padded_out, int64_t max_rank)
{
    for (int64_t d = 0; d < max_rank; d++) {
        int64_t ref = -1;
        for (size_t i = 0; i < padded_in.size(); i++) {
            if (padded_in[i][d] != 1) {
                if (ref == -1)
                    ref = padded_in[i][d];
                else if (padded_in[i][d] != ref) {
                    OP_LOGE("CheckBroadcastShape", "dim %d broadcast incompatible: input[%d] size %d != %d", (int)d,
                            (int)i, (int)padded_in[i][d], (int)ref);
                    return false;
                }
            }
        }
        for (size_t i = 0; i < padded_out.size(); i++) {
            if (padded_out[i][d] != 1) {
                if (ref == -1)
                    ref = padded_out[i][d];
                else if (padded_out[i][d] != ref) {
                    OP_LOGE("CheckBroadcastShape", "dim %d broadcast incompatible: output[%d] size %d != %d", (int)d,
                            (int)i, (int)padded_out[i][d], (int)ref);
                    return false;
                }
            }
        }
    }
    return true;
}

// ComputeBroadcastDims — 从等 rank padded shapes 中筛选非全 1 维，构建广播坐标和归一化 shape
static void ComputeBroadcastDims(const std::vector<std::vector<int64_t>>& padded_in,
                                 const std::vector<std::vector<int64_t>>& padded_out, int64_t max_rank,
                                 std::vector<int64_t>& bro_shape, std::vector<std::vector<int64_t>>& norm_in,
                                 std::vector<std::vector<int64_t>>& norm_out)
{
    for (int64_t d = 0; d < max_rank; d++) {
        bool all_one = true;
        int64_t max_dim = 0;
        for (size_t i = 0; i < padded_in.size(); i++) {
            if (padded_in[i][d] != 1)
                all_one = false;
            max_dim = std::max(max_dim, padded_in[i][d]);
        }
        for (size_t i = 0; i < padded_out.size(); i++) {
            if (padded_out[i][d] != 1)
                all_one = false;
            max_dim = std::max(max_dim, padded_out[i][d]);
        }
        if (!all_one) {
            bro_shape.push_back(max_dim);
            for (size_t i = 0; i < padded_in.size(); i++)
                norm_in[i].push_back(padded_in[i][d]);
            for (size_t i = 0; i < padded_out.size(); i++)
                norm_out[i].push_back(padded_out[i][d]);
        }
    }
    if (bro_shape.empty()) {
        bro_shape.push_back(1);
        for (auto& v : norm_in)
            v.push_back(1);
        for (auto& v : norm_out)
            v.push_back(1);
    }
}

// PadAndSqueeze — 将 input/output 的不同 shape 归一化到最大广播坐标系
bool PadAndSqueeze(const std::vector<std::vector<int64_t>>& input_shapes,
                   const std::vector<std::vector<int64_t>>& output_shapes, std::vector<int64_t>& maximum_bro_shape,
                   std::vector<std::vector<int64_t>>& normal_input_shapes,
                   std::vector<std::vector<int64_t>>& normal_output_shapes)
{
    int64_t num_inputs = (int64_t)input_shapes.size();
    int64_t num_outputs = (int64_t)output_shapes.size();
    int64_t max_rank = 0;
    for (auto& s : input_shapes)
        max_rank = std::max(max_rank, (int64_t)s.size());
    for (auto& s : output_shapes)
        max_rank = std::max(max_rank, (int64_t)s.size());
    auto pad = [&](const std::vector<int64_t>& s) {
        std::vector<int64_t> p;
        p.assign(max_rank - (int64_t)s.size(), 1);
        p.insert(p.end(), s.begin(), s.end());
        return p;
    };
    std::vector<std::vector<int64_t>> padded_in(num_inputs), padded_out(num_outputs);
    for (int64_t i = 0; i < num_inputs; i++)
        padded_in[i] = pad(input_shapes[i]);
    for (int64_t i = 0; i < num_outputs; i++)
        padded_out[i] = pad(output_shapes[i]);
    maximum_bro_shape.clear();
    normal_input_shapes.assign(num_inputs, std::vector<int64_t>());
    normal_output_shapes.assign(num_outputs, std::vector<int64_t>());
    ComputeBroadcastDims(padded_in, padded_out, max_rank, maximum_bro_shape, normal_input_shapes, normal_output_shapes);
    return true;
}

// FindSplitAxis — 确定 UB 切分轴和 tile 大小
bool FindSplitAxis(const std::vector<int64_t>& max_bro_shape, int64_t dtype_size, int64_t ub_per_core,
                   int64_t phys_nodes, SplitResult& out)
{
    // 除零保护: phys_nodes / dtype_size 为除数，必须 > 0
    if (phys_nodes <= 0 || dtype_size <= 0) {
        OP_LOGE("FindSplitAxis", "invalid divisor: phys_nodes=%d dtype_size=%d", (int)phys_nodes, (int)dtype_size);
        return false;
    }
    int64_t per_buf_bytes = (ub_per_core / phys_nodes) & ~(kUbAlignBytes - 1); // UB 对齐, TBuf 硬件要求
    int64_t per_buf_elems = per_buf_bytes / dtype_size;
    // 单 buffer 至少能容纳 1 个元素，否则后续 per_buf_elems/inner、%a_i 会除零
    if (per_buf_elems <= 0) {
        OP_LOGE("FindSplitAxis", "per_buf_elems=%d invalid (ub_per_core=%d phys_nodes=%d dtype_size=%d)",
                (int)per_buf_elems, (int)ub_per_core, (int)phys_nodes, (int)dtype_size);
        return false;
    }
    int64_t rank = (int64_t)max_bro_shape.size();
    int64_t inner = 1;
    for (int64_t k = rank - 1; k >= 0; k--) {
        if (max_bro_shape[k] * inner > per_buf_elems) {
            out.a_i = per_buf_elems / inner;
            // 除零保护: 单行内层维度乘积超过单 buffer 容量时 a_i 会被整除成 0，
            // 导致下方 /a_i、%a_i 除零。此 shape 当前不支持，返回失败。
            if (out.a_i <= 0) {
                OP_LOGE("FindSplitAxis",
                        "inner dims product exceeds per-buffer capacity (per_buf_elems=%d, inner=%d) at axis %d; shape "
                        "unsupported",
                        (int)per_buf_elems, (int)inner, (int)k);
                return false;
            }
            out.a_o = (max_bro_shape[k] + out.a_i - 1) / out.a_i;
            int64_t rem = max_bro_shape[k] % out.a_i;
            out.a_i_tail = (rem == 0) ? out.a_i : rem;
            out.axis = k;
            return true;
        }
        if (k == 0) {
            out.axis = 0;
            out.a_i = max_bro_shape[0];
            out.a_o = 1;
            out.a_i_tail = max_bro_shape[0];
            return true;
        }
        inner *= max_bro_shape[k];
    }
    return true;
}

// MultiCoreSplit — 多核任务划分
bool MultiCoreSplit(const std::vector<int64_t>& max_bro_shape, const SplitResult& ub_split, int64_t max_cores,
                    MultiCoreResult& out)
{
    int64_t k = ub_split.axis, outer_prod = 1;
    for (int64_t j = 0; j < k; j++)
        outer_prod *= max_bro_shape[j];
    out.total_tiles = outer_prod * ub_split.a_o;
    out.num_cores = (out.total_tiles < max_cores) ? out.total_tiles : max_cores;
    // 除零保护: num_cores 为除数。total_tiles==0（空 tensor / 0 维）或 max_cores<=0
    // 都会使 num_cores<=0，导致下方 /num_cores、%num_cores 除零。
    if (out.num_cores <= 0) {
        OP_LOGE("MultiCoreSplit", "num_cores=%d invalid (total_tiles=%d max_cores=%d)", (int)out.num_cores,
                (int)out.total_tiles, (int)max_cores);
        return false;
    }
    out.tiles_main = out.total_tiles / out.num_cores;
    out.cores_tail = out.total_tiles % out.num_cores;
    return true;
}

// 地址偏移计算 — input/output 的 stride 计算逻辑一致，合并为一个函数
bool PrecomputeStrides(const std::vector<int64_t>& s, std::vector<int64_t>& strides)
{
    int64_t rank = (int64_t)s.size();
    strides.assign(rank, 0);
    for (int64_t d = rank - 1; d >= 0; d--) {
        if (s[d] == 1) {
            strides[d] = 0;
            continue;
        }
        int64_t prod = 1;
        for (int64_t j = d + 1; j < rank; j++)
            prod *= s[j];
        strides[d] = prod;
    }
    return true;
}

