// UT tiling-definition shim for softplus_v2_grad.
//
// The op_kernel entry uses REGISTER_NONE_TILING, which the build-system tiling-header
// generator does not recognize (it only handles REGISTER_TILING_DEFAULT / BroadcastSch),
// so GET_TILING_DATA_WITH_STRUCT is not auto-generated for this op. When this
// <op>_tiling_def.h exists, ut.cmake force-includes it (and skips generation), so we
// define the macro here. The kernel simply needs a local TilingData struct populated
// from the raw tiling GM buffer; a byte copy is exactly what the generated macro does
// (minus the internal REGISTER_TILINGDATA_SIZE bookkeeping, which is unnecessary for UT).

#ifndef SOFTPLUS_V2_GRAD_TILING_DEF_H_
#define SOFTPLUS_V2_GRAD_TILING_DEF_H_

#include <cstring>

#ifndef GET_TILING_DATA_WITH_STRUCT
#define GET_TILING_DATA_WITH_STRUCT(tiling_struct, tiling_data, tiling_arg) \
    tiling_struct tiling_data;                                              \
    std::memcpy(reinterpret_cast<void*>(&tiling_data), reinterpret_cast<const void*>(tiling_arg), sizeof(tiling_struct))
#endif

#endif // SOFTPLUS_V2_GRAD_TILING_DEF_H_
