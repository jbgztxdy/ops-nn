    AscendC::SetKernelMode(KernelMode::AIV_MODE);
    ICPU_SET_TILING_KEY(0);
    ICPU_RUN_KF(invoker, blockDim, grad, feat, out, workspace, tiling);

    WriteFile(path + "/softplus_v2_grad_data/" + tag + "_npuout.bin", out, outBytes);

    AscendC::GmFree(grad);
    AscendC::GmFree(feat);
    AscendC::GmFree(out);
    AscendC::GmFree(workspace);

    // compare_data.py exits non-zero on mismatch; RunCompareData returns false in that case.
    bool ok = kernel_ut::RunCompareData("./softplus_v2_grad_data", {tag, dt.pyName});
    EXPECT_TRUE(ok) << "golden compare failed for tag=" << tag;
