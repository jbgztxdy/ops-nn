constexpr int64_t kUB = 253952; // UB size (matches host compile info UB_SIZE)

// FindSplitAxis — verbatim algorithm from host tiling.
static bool FindSplitAxis(const std::vector<int64_t>& max_bro_shape, int64_t dtype_size, int64_t ub_per_core,
                          int64_t phys_nodes, SplitResult& out)
{
    if (phys_nodes <= 0 || dtype_size <= 0)
        return false;
    int64_t per_buf_bytes = (ub_per_core / phys_nodes) & ~(kUbAlignBytes - 1);
    int64_t per_buf_elems = per_buf_bytes / dtype_size;
    if (per_buf_elems <= 0)
        return false;
    int64_t rank = (int64_t)max_bro_shape.size();
    int64_t inner = 1;
    for (int64_t k = rank - 1; k >= 0; k--) {
        if (max_bro_shape[k] * inner > per_buf_elems) {
            out.a_i = per_buf_elems / inner;
            if (out.a_i <= 0)
                return false;
            out.a_o = (max_bro_shape[k] + out.a_i - 1) / out.a_i;
            int64_t rem = max_bro_shape[k] % out.a_i;
            out.a_i_tail = (rem == 0) ? out.a_i : rem;
            out.axis = k;
            return true;
        }
        if (k == 0) {
            out.axis = 0;
            out.a_i = max_bro_shape[0];
            out.a_o = 1;
            out.a_i_tail = max_bro_shape[0];
            return true;
        }
        inner *= max_bro_shape[k];
    }
    return true;
}

// MultiCoreSplit — verbatim algorithm from host tiling.
static bool MultiCoreSplit(const std::vector<int64_t>& max_bro_shape, const SplitResult& ub_split, int64_t max_cores,
                           MultiCoreResult& out)
{
    int64_t k = ub_split.axis, outer_prod = 1;
    for (int64_t j = 0; j < k; j++)
        outer_prod *= max_bro_shape[j];
    out.total_tiles = outer_prod * ub_split.a_o;
    out.num_cores = (out.total_tiles < max_cores) ? out.total_tiles : max_cores;
    if (out.num_cores <= 0)
        return false;
    out.tiles_main = out.total_tiles / out.num_cores;
    out.cores_tail = out.total_tiles % out.num_cores;
    return true;
}

// PrecomputeStrides — verbatim from host tiling (size-1 dims -> stride 0).
static void PrecomputeStrides(const std::vector<int64_t>& s, std::vector<int64_t>& strides)
{
    int64_t rank = (int64_t)s.size();
    strides.assign(rank, 0);
    for (int64_t d = rank - 1; d >= 0; d--) {
        if (s[d] == 1) {
            strides[d] = 0;
            continue;
        }
        int64_t prod = 1;
        for (int64_t j = d + 1; j < rank; j++)
            prod *= s[j];
        strides[d] = prod;
    }
}

// Front-pad src[0..n) into dst[delta..delta+n), dst[0..delta)=padValue.
static void PadRow(int64_t* dst, const int64_t* src, int64_t n, int64_t delta, int64_t padValue)
{
    for (int64_t d = 0; d < delta; d++)
        dst[d] = padValue;
    for (int64_t d = 0; d < n; d++)
        dst[d + delta] = src[d];
}

// Build a SoftplusV2GradTilingData<R> from already-squeezed max_bro_shape + per-slot normal
// shapes (all rank_ length, no dims squeezed for the shapes chosen in these tests).
// Returns false if tiling is infeasible.
template <int64_t R>
static bool BuildTiling(uint8_t* buf, const std::vector<int64_t>& max_bro_shape,
                        const std::vector<std::vector<int64_t>>& in_shapes, const std::vector<int64_t>& out_shape,
                        float beta, float threshold, int64_t core_num)
{
    auto* t = reinterpret_cast<SoftplusV2GradTilingData<R>*>(buf);
    int64_t rank_ = (int64_t)max_bro_shape.size();
    int64_t delta = R - rank_;
    if (delta < 0)
        return false;

    int64_t per_buf_bytes = (kUB / kPhysNodes) & ~(kUbAlignBytes - 1);
    if (!FindSplitAxis(max_bro_shape, sizeof(float), kUB, kPhysNodes, t->split))
        return false;
    if (!MultiCoreSplit(max_bro_shape, t->split, core_num, t->multicore))
        return false;

    t->per_buf_bytes = per_buf_bytes;
    t->beta = beta;
    t->threshold = threshold;
    t->rank = rank_;
    t->num_inputs = (int64_t)in_shapes.size();
    t->num_outputs = 1;

    // max_bro_shape: front-pad 1.
    PadRow(t->max_bro_shape, max_bro_shape.data(), rank_, delta, 1);
    // split axis shifted right by delta.
    t->split.axis += delta;

    // input slots
    for (int64_t i = 0; i < kMaxInputSlots; i++) {
        if (i < (int64_t)in_shapes.size()) {
            std::vector<int64_t> st;
            PrecomputeStrides(in_shapes[i], st);
            PadRow(t->input_shapes[i], in_shapes[i].data(), rank_, delta, 1);
            PadRow(t->input_strides[i], st.data(), rank_, delta, 0);
        } else {
            for (int64_t d = 0; d < R; d++) {
                t->input_shapes[i][d] = 1;
                t->input_strides[i][d] = 0;
            }
        }
    }
    // output slot 0
    {
        std::vector<int64_t> st;
        PrecomputeStrides(out_shape, st);
        PadRow(t->output_shapes[0], out_shape.data(), rank_, delta, 1);
        PadRow(t->output_strides[0], st.data(), rank_, delta, 0);
    }
    return true;
}

