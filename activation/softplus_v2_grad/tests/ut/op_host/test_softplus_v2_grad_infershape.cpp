#include <gtest/gtest.h>
#include <iostream>
#include "infershape_test_util.h"
#include "ut_op_common.h"
#include "log/log.h"
#include "../../../op_graph/softplus_v2_grad_proto.h"

class SoftplusV2Grad : public testing::Test {
protected:
    static void SetUpTestCase() { std::cout << "SoftplusV2Grad InferShape SetUp" << std::endl; }

    static void TearDownTestCase() { std::cout << "SoftplusV2Grad InferShape TearDown" << std::endl; }
};

// 同 shape 广播: 2 输入相同 → 输出相同
TEST_F(SoftplusV2Grad, SoftplusV2Grad_infershape_same_shape)
{
    ge::op::SoftplusV2Grad op;
    op.UpdateInputDesc("input_gradients", create_desc({4, 1, 1280}, ge::DT_FLOAT16));
    op.UpdateInputDesc("input_features", create_desc({4, 1, 1280}, ge::DT_FLOAT16));

    EXPECT_EQ(InferShapeTest(op), ge::GRAPH_SUCCESS);
}

// 可广播 shape: {4,1,1280} + {1,3,1280} → {4,3,1280}
TEST_F(SoftplusV2Grad, SoftplusV2Grad_infershape_broadcast)
{
    ge::op::SoftplusV2Grad op;
    op.UpdateInputDesc("input_gradients", create_desc({4, 1, 1280}, ge::DT_FLOAT));
    op.UpdateInputDesc("input_features", create_desc({1, 3, 1280}, ge::DT_FLOAT));

    EXPECT_EQ(InferShapeTest(op), ge::GRAPH_SUCCESS);
}

// rank 不同 + 标量广播: {2,3,4} + {4}
TEST_F(SoftplusV2Grad, SoftplusV2Grad_infershape_rank_diff)
{
    ge::op::SoftplusV2Grad op;
    op.UpdateInputDesc("input_gradients", create_desc({2, 3, 4}, ge::DT_BF16));
    op.UpdateInputDesc("input_features", create_desc({4}, ge::DT_BF16));

    EXPECT_EQ(InferShapeTest(op), ge::GRAPH_SUCCESS);
}
