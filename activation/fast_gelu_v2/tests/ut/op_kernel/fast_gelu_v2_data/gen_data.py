def fast_gelu_v2_numpy(x):
    eps = 1e-12
    x_eps = x + eps
    abs_x_eps = np.abs(x_eps)
    sgn_x = x_eps / abs_x_eps
    sgn_x = np.clip(sgn_x, -1.0, 1.0)

    abs_x = np.abs(x)
    scaled_x = 0.7071 * abs_x
    clipped_x = np.minimum(scaled_x, 1.769)
    diff = clipped_x - 1.769
    sq = diff * diff
    inner = -0.1444 * sq + 0.5
    bracket = sgn_x * inner + 0.5
    return x * bracket


