    bool bIsNan = (bExp == DOUBLE_EXP_MASK) && (bMan != 0);
    bool aIsInf = (aExp == DOUBLE_EXP_MASK) && (aMan == 0);
    bool bIsInf = (bExp == DOUBLE_EXP_MASK) && (bMan == 0);
    // NaN + anything = NaN
    if (aIsNan || bIsNan) return QUIET_NAN;
    // Inf + (-Inf) = NaN, (-Inf) + Inf = NaN
